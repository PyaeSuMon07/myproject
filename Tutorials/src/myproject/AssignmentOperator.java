package myproject;

public class AssignmentOperator {
    public static void main(String[]args) {
        int x=5,y=10;
        System.out.println(x);
        x +=6;
        System.out.println(x);
        x -=4;
        System.out.println(x);
        y *=5;
        System.out.println(y);
        y /=2;
        System.out.println(y);
        y %=4;
        System.out.println(y);
    }
}
